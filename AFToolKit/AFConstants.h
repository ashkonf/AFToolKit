#define SECONDS_IN_DAY (60 * 60 * 24)
#define SECONDS_IN_WEEK (SECONDS_IN_DAY * 7)
#define SECONDS_IN_MONTH (SECONDS_IN_DAY * 30)
#define SECONDS_IN_YEAR (SECONDS_IN_DAY * 365)
